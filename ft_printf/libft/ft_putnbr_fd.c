#include <unistd.h>

void	ft_putnbr_fd(int nb, int fd)
{
	int	q;
	int	r;

	if (nb >= 0)
	{
		q = nb / 10;
		r = nb % 10 + 48;
	}
	else if (nb == -2147483648)
	{
		q = 214748364;
		r = 8 + 48;
		write(fd, "-", 1);
	}
	else
	{
		q = (0 - nb) / 10;
		r = (0 - nb) % 10 + 48;
		write(fd, "-", 1);
	}
	if (q != 0)
		ft_putnbr_fd(q, fd);
	write(fd, &r, 1);
}
