#include "libft.h"

void	ft_lstclear(t_list **lst, void (*del)(void *))
{
	t_list	*temp;

	temp = *lst;
	while (*lst)
	{
		del((*lst)->content);
		temp = ((*lst)->next);
		free(*lst);
		*lst = temp;
	}
}
